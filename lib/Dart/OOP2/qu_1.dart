// what is Encapsulation In Dart ?
/*
Encapsulation is a mechanism for hiding an object's implementation details, 
protecting the internal code and internal data from being accessed directly. 
It is one of the fundamental pillars of object-oriented programming 
and is crucial for code maintenance and error prevention.
- Encapsulation is the principle that limits access to an object's state;
 and the bundling of methods and operations that do work on a set of data.
The whole idea behind encapsulation is to hide the implementation details from users. 

- Benefits of Encapsulation
Data Hiding: The users will have no idea about the implementation details of the class.
 It will not be visible to the user how the class is storing the values.
  The user will only know that values are being passed and initialized.
More Flexibility: We can make the variables of the class read-only or write-only according to our requirements. 
If we want to make the variables read-only then we have to use the setter methods like setName(), SetAge(), etc,
 and if we want to make the variables write only then we need to use the getter methods like getName(), getValue(),
  etc from the program.
Reusability: Encapsulation also provides the feature of code reusability and is easy to change with newer requirements.
Easy Testing: The encapsulated code is easy to test for unit testing.

- In Dart, Encapsulation means hiding data within a library, preventing it from outside factors. 
It helps you control your program and prevent it from becoming too complicated.
 */


