// How To Achieve Encapsulation In Dart?

/* - Declaring the class properties as private by using underscore(_).
   - Providing public getter and setter methods to access and update the value of private property.



In Dart, you can achieve encapsulation by using private and public access specifiers.

Public and Private Modifiers
Public: By default, all members (fields, methods, getters, and setters) are public in Dart.
 They can be accessed from outside the class.
Private: You can make a member private by prefixing its name with an underscore _. 
Private members can only be accessed within the same library where they are defined.

 */