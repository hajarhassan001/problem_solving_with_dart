// What Is Library In Dart?
/*A library in a programming language represents a collection of routines (set of programming instructions).
 Dart has a set of built-in libraries that are useful to store routines that are frequently used. 
A Dart library comprises of a set of classes, constants, functions, typedefs, properties, and exceptions.

- By default, every .dart file is a library. A library is a collection of functions and classes.
 A library can be imported into another library using the import keyword.
