// What's problem Of Copy Paste Instead Of Inheritance?


/*If you copy the code from one class to another class, then you will have to maintain the code in both the classes.
 If you make any changes in one class,
 then you will have to make the same changes in the other class. This can lead to errors and bugs in the code.
 
 
 - The point is that copy and paste is cheap for solving the immediate problem. 
 The real issue is that in the medium/long term the cost of maintaining duplicated code is far higher than well-factored code */